package deposit.model;

/**
 *
 * @author gustavo
 */
public class Deposit {
    
    private String socialName;
    private String cpf;
    private String number;
    private String fantasyName;
    private String cnpj;
    private String password;
    private int posX;
    private int posY;
    
    /**
     * Constructor of this class.
     * @param cpf
     * @param cnpj
     * @param socialName
     * @param fantasyName
     * @param number
     * @param password
     * @param posX
     * @param posY 
     */
    public Deposit(String cpf, String cnpj, String socialName, String fantasyName, String number, String password, int posX, int posY){
        
        this.cpf = cpf;
        this.cnpj = cnpj;
        this.socialName = socialName;
        this.fantasyName = fantasyName;
        this.number = number;
        this.password = password;
        this.posX = posX;
        this.posY = posY;
    }

                                                /* Encapsulate Fields */
    /**
     * @return the socialName
     */
    public String getSocialName() {
        return socialName;
    }

    /**
     * @param socialName the socialName to set
     */
    public void setSocialName(String socialName) {
        this.socialName = socialName;
    }

    /**
     * @return the cpf
     */
    public String getCpf() {
        return cpf;
    }

    /**
     * @param cpf the cpf to set
     */
    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    /**
     * @return the number
     */
    public String getNumber() {
        return number;
    }

    /**
     * @param number the number to set
     */
    public void setNumber(String number) {
        this.number = number;
    }

    /**
     * @return the fantasyName
     */
    public String getFantasyName() {
        return fantasyName;
    }

    /**
     * @param fantasyName the fantasyName to set
     */
    public void setFantasyName(String fantasyName) {
        this.fantasyName = fantasyName;
    }

    /**
     * @return the cnpj
     */
    public String getCnpj() {
        return cnpj;
    }

    /**
     * @param cnpj the cnpj to set
     */
    public void setCnpj(String cnpj) {
        this.cnpj = cnpj;
    }

    /**
     * @return the password
     */
    public String getPassword() {
        return password;
    }

    /**
     * @param password the password to set
     */
    public void setPassword(String password) {
        this.password = password;
    }

    /**
     * @return the posX
     */
    public int getPosX() {
        return posX;
    }

    /**
     * @param posX the posX to set
     */
    public void setPosX(int posX) {
        this.posX = posX;
    }

    /**
     * @return the posY
     */
    public int getPosY() {
        return posY;
    }

    /**
     * @param posY the posY to set
     */
    public void setPosY(int posY) {
        this.posY = posY;
    }
}
